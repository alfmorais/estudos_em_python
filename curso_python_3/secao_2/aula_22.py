'''
Entrada de dados com a função input()
'''

nome = input('Qual é o seu nome? ')
print(f'O usuário digitou: {nome}')
print(f'O tipo da variavel é {type(nome)}')

idade = input('Qual é a sua idade? ')
print(f'O usuário {nome}, tem {idade} anos')
print(f'O tipo da variavel é {type(idade)}')
