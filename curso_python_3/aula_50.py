"""
Funções - def em Python (Parte 1)
"""

# Função imprimindo os valores


def function():
    print('Hello World')


# chamando uma função
function()

# definindo uma função com parametro de origem


def saudacao(msg='vai se fuder'):
    print(msg)


# usando com uma mensagem
saudacao('Você consegue!')

# usando sem mensagem
saudacao()
